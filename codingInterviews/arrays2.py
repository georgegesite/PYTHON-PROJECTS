# Reverse
nums = [1, 2, 3]
nums.reverse()
print(nums)

# Sorting
arr = [5, 4, 7, 3, 8]
arr.sort()
print(arr)

#Sort in reverse
arr.sort(reverse=True)
print(arr)

#Sort Alphabetically
arr = ["bob", "alice", "jane", "doe"]
arr.sort()
print(arr)

# List comprehension
arr = [i for i in range(5)]
print(arr)

# 2-D lists
arr = [[0] * 4 for i in range(4)]
print(arr)
print(arr[0][0], arr[3][3])